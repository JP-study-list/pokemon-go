/** 取得目前語言的文字；%s 會被 arg 取代 */
export function makeT(lang) {
  const dict = STRINGS[lang] || STRINGS[DEFAULT_LANG];
  return (key, arg) => {
    const s = dict[key] ?? key;
    return arg === undefined ? s : s.replace("%s", arg);
  };
}
